#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	size_t	*ptr;

	if (count * size > INT_MAX)
		return (NULL);
	ptr = malloc(count * size);
	if (!ptr)
		return (NULL);
	ft_bzero(ptr, count * size);
	return (ptr);
}
